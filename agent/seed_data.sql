-- ==========================================
-- 1. BASE AUTH & USERS
-- ==========================================

CREATE TABLE IF NOT EXISTS public.users (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  email text NOT NULL UNIQUE,
  password_hash text NOT NULL,
  display_name text,
  created_at timestamp with time zone NOT NULL DEFAULT now()
);

CREATE TABLE public.students (
  id uuid PRIMARY KEY DEFAULT gen_random_uuid(),
  teacher_id uuid NOT NULL,
  student_id text NOT NULL,
  name text NOT NULL,

  created_at timestamp with time zone NOT NULL DEFAULT now(),

  CONSTRAINT students_teacher_uuid_unique UNIQUE (teacher_id, id),
  CONSTRAINT students_teacher_student_id_unique UNIQUE (teacher_id, student_id),

  CONSTRAINT students_teacher_id_fkey
    FOREIGN KEY (teacher_id)
    REFERENCES public.users(id)
);

-- ==========================================
-- 2. ASSIGNMENTS
-- ==========================================

CREATE TABLE public.assignments (
  id integer GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  teacher_id uuid NOT NULL,
  title text NOT NULL,
  subject text,
  total_marks integer,
  created_at timestamp with time zone NOT NULL DEFAULT now(),
  CONSTRAINT assignments_pkey PRIMARY KEY (id),
  CONSTRAINT assignments_teacher_id_fkey 
    FOREIGN KEY (teacher_id) 
    REFERENCES public.users(id)
);

-- ==========================================
-- 3. QUESTIONS
-- ==========================================

CREATE TABLE public.questions (
  id integer GENERATED BY DEFAULT AS IDENTITY NOT NULL,

  teacher_id uuid NOT NULL,
  assignment_id integer NOT NULL,

  question_label text NOT NULL,
  question_description text NOT NULL,

  marks integer,

  created_at timestamp with time zone DEFAULT now(),

  CONSTRAINT questions_pkey PRIMARY KEY (id),

  CONSTRAINT questions_teacher_fkey
    FOREIGN KEY (teacher_id)
    REFERENCES public.users(id),

  CONSTRAINT questions_assignment_fkey
    FOREIGN KEY (assignment_id)
    REFERENCES public.assignments(id)
);

-- ==========================================
-- 4. RUBRICS
